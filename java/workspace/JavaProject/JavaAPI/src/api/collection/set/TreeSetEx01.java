package api.collection.set;

import java.util.TreeSet;

public class TreeSetEx01 {

	public static void main(String[] args) {
		
		//TreeSet = 정렬 된 셋
		//사용방법은 Hashset과 동일함
		
		TreeSet<String> set = new TreeSet<>();
		
		set.add("홍길동");
		set.add("홍길자");
		set.add("짱구");
		set.add("철수");
		set.add("훈이");
		set.add("유리");
		
		
		System.out.println(set.toString());
		
		//나머지 기능은 HashSet과 동일함.
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}
}
